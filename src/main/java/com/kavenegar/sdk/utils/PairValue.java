package com.kavenegar.sdk.utils;

/**
 *
 * @author Hadi
 */
public class PairValue {
    private String Key=null;
    private String Value=null;

    public PairValue(String Key,String Value)
    {
        this.Key=Key;
        this.Value=Value;
    }

    public String getKey() {
        return Key;
    }

    public String getValue() {
        return Value;
    }

    
    
    
    
    
    
}
